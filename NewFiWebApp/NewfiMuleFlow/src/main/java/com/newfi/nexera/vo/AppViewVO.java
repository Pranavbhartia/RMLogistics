package com.newfi.nexera.vo;

/**
 * @author Utsav
 *
 */
public class AppViewVO
{
    private String opName;

    private String sTicket;

    private String sLoanName;


    /**
     * @return the opName
     */
    public String getOpName()
    {
        return opName;
    }


    /**
     * @param opName the opName to set
     */
    public void setOpName( String opName )
    {
        this.opName = opName;
    }


    /**
     * @return the sTicket
     */
    public String getsTicket()
    {
        return sTicket;
    }


    /**
     * @param sTicket the sTicket to set
     */
    public void setsTicket( String sTicket )
    {
        this.sTicket = sTicket;
    }


    /**
     * @return the sLoanName
     */
    public String getsLoanName()
    {
        return sLoanName;
    }


    /**
     * @param sLoanName the sLoanName to set
     */
    public void setsLoanName( String sLoanName )
    {
        this.sLoanName = sLoanName;
    }


}
